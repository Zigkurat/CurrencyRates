package com.sfl.sflassignment.network.model;

public class ExchangeRate {
    private float buy;
    private float sell;

    public float getBuy() {
        return buy;
    }

    public void setBuy(float buy) {
        this.buy = buy;
    }

    public float getSell() {
        return sell;
    }

    public void setSell(float sell) {
        this.sell = sell;
    }
}
